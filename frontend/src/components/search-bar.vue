<script setup>
import { ref, watch } from 'vue';

// --- STATE ---
const searchQuery = ref(''); // Lưu trữ nội dung input (thay thế value="{{ request('q') }}")
const suggestions = ref([]); // Danh sách gợi ý tìm kiếm
const showSuggestions = ref(false); // Trạng thái hiển thị/ẩn thanh gợi ý
let searchTimeout = null; // Biến để xử lý Debouncing

// --- METHODS ---

// Hàm xử lý logic tìm kiếm (Đã được Debounce)
const fetchSuggestions = (query) => {
    if (query.length < 2) {
        suggestions.value = [];
        showSuggestions.value = false;
        return;
    }
    
    // --- THỰC HIỆN GỌI API TẠI ĐÂY ---
    console.log(`Đang tìm kiếm gợi ý cho: ${query}`);

    // Giả lập dữ liệu API (Mock Data)
    const mockData = [
        { id: 1, title: 'iPhone 13 Pro Max (25tr)', price: '25,000,000', image_url: 'https://via.placeholder.com/40/3B82F6/FFFFFF?text=P1' },
        { id: 2, title: 'MacBook Air M1 (22tr)', price: '22,000,000', image_url: 'https://via.placeholder.com/40/10B981/FFFFFF?text=P2' },
        { id: 3, title: 'Sách Kinh tế học (450k)', price: '450,000', image_url: 'https://via.placeholder.com/40/8B5CF6/FFFFFF?text=P3' }
    ];

    if (mockData.length > 0) {
        suggestions.value = mockData;
        showSuggestions.value = true;
    } else {
        suggestions.value = [];
        showSuggestions.value = false;
    }
};

// Hàm xử lý Debounce cho input
const handleInput = () => {
    // Xóa timeout cũ nếu có
    if (searchTimeout) {
        clearTimeout(searchTimeout);
    }
    
    // Thiết lập timeout mới sau 300ms
    searchTimeout = setTimeout(() => {
        fetchSuggestions(searchQuery.value.trim());
    }, 300);
};

// Hàm xử lý khi người dùng nhấn nút Tìm hoặc Enter
const handleSubmit = () => {
    if (searchQuery.value.trim().length > 0) {
        console.log(`Thực hiện tìm kiếm chính thức: /products?q=${searchQuery.value}`);
        // Thay thế bằng router.push() hoặc Inertia.visit()
    }
};

// Hàm xử lý khi click ra ngoài (để ẩn suggestions)
const hideSuggestions = () => {
    // Chỉ ẩn suggestions, không xóa query
    showSuggestions.value = false;
};

// Tự động gán giá trị request('q') nếu có (Dùng cho Page được tải lần đầu)
// Trong môi trường Vue/Inertia, query ban đầu sẽ được truyền qua props nếu cần.
// Ở đây tôi giả định nó không được truyền qua props và khởi tạo là rỗng.

</script>

<template>
    <!-- Gói trong div thay vì form nếu dùng Vue Router để quản lý định tuyến -->
    <div v-click-away="hideSuggestions" class="relative">
        <div class="relative">
            <!-- Sử dụng v-model để liên kết với searchQuery -->
            <input 
                type="text" 
                v-model="searchQuery"
                @input="handleInput"
                @keyup.enter="handleSubmit"
                placeholder="Tìm kiếm sản phẩm..." 
                class="w-full pl-10 pr-16 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-transparent"
                autocomplete="off"
            >
            <div class="absolute inset-y-0 left-0 pl-3 flex items-center pointer-events-none">
                <i class="fas fa-search text-gray-400"></i>
            </div>
            <!-- Sử dụng @click.prevent="handleSubmit" để ngăn form submit mặc định -->
            <button @click.prevent="handleSubmit" type="submit" class="absolute inset-y-0 right-0 pr-1 flex items-center">
                <span class="bg-blue-600 text-white px-3 py-1 rounded-lg text-sm hover:bg-blue-700">
                    Tìm
                </span>
            </button>
        </div>
        
        <!-- Search Suggestions -->
        <!-- Logic hiển thị/ẩn thay thế cho class hidden/remove('hidden') của JS -->
        <div v-show="showSuggestions" class="absolute top-full left-0 right-0 bg-white border border-gray-200 rounded-lg shadow-lg mt-1 z-50">
            <!-- Dynamic content will be loaded here using v-for -->
            <template v-if="suggestions.length > 0">
                <a 
                    v-for="item in suggestions"
                    :key="item.id"
                    href="#" 
                    class="block px-4 py-2 hover:bg-gray-50 flex items-center space-x-3"
                >
                    <img :src="item.image_url" :alt="item.title" class="w-10 h-10 object-cover rounded">
                    <div>
                        <p class="text-sm font-medium text-gray-900">{{ item.title }}</p>
                        <p class="text-xs text-gray-500">{{ item.price }}₫</p>
                    </div>
                </a>
            </template>
            <div v-else class="px-4 py-2 text-sm text-gray-500">
                Không tìm thấy gợi ý nào.
            </div>
        </div>
    </div>
</template>

